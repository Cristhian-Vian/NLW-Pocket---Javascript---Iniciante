// Exemplo de declaração de váriaveis e utilizações

// Variável global constante
const mensagem = "Olá, mundo!";
console.log(mensagem);

// Variável local
{
    const mensagem = "Olá, submundo!";
    console.log(mensagem);
}

// Variavel mutavel let
let mensagem2 = "Agora sou um texto...";
console.log(mensagem2);

mensagem2 = 7
console.log(`Agora sou um número: ` + mensagem2);